package com.game.combat;

public class TempStat {
    public int hp, mp, atk, def, agi, crit;
    public TempStat(int hp,int mp, int atk, int def, int agi,int crit) {
        this.hp = hp;
        this.mp = mp;
        this.atk = atk;
        this.def = def;
        this.agi = agi;
        this.crit = crit;
    }
}
